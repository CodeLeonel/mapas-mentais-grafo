package aplicacao;

public class IntervaloPadrao {

	private Integer inicio;
	
	private Integer fim;
	
	public IntervaloPadrao(Integer inicio, Integer fim) {
		this.inicio = inicio;
		this.fim = fim;
	}

	public Integer getInicio() {
		return inicio;
	}

	public Integer getFim() {
		return fim;
	}	
	
}
